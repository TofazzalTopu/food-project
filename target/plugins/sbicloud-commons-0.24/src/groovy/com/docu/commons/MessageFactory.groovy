public class MessageFactory {

  public static Message getMessage(int type) {
    return new Message(type: type)
  }

  public static Message getErrorMessage() {
    return new Message(type: Message.ERROR)
  }

  public static Message getSuccessMessage() {
    return new Message(type: Message.SUCCESS)
  }

  public static Message getWaringMessage() {
    return new Message(type: Message.WARNING)
  }

  public static Message getConfirmationMessage() {
    return new Message(type: Message.CONFIRMATION)
  }

  public static Message getInformationMessage() {
    return new Message(type: Message.INFORMATION)
  }
}
